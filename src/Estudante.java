public class Estudante {

    private String Nome;
    private int Idade;
    private int Pontos;

    public String getNome(){

        return Nome;
    }
    public void setNome(String nome){

        Nome = nome;
    }

    public int getIdade(){

        return Idade;
    }
    public void setIdade(int idade){

        Idade = idade;
    }

    public int getPontos(){

        return Pontos;
    }
    public void setPontos(int pontos){

        Pontos = pontos;
    }
}
